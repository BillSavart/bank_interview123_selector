// 「試場資訊」的資料來源。涵蓋兩種階段（stage）：
//   - 'written'   筆試：主要統計各試場的缺考人數。
//   - 'interview' 面試：彙整各試場的面試題目 / 考驗重點。
//
// 每一場（某銀行、某日期、某階段）整理成一筆 VenueReport，依日期由新到舊排在
// VENUE_REPORTS 陣列。之後每有新資料，只要在最前面再加一筆即可，頁面與左側選單會
// 自動帶出來——不需要後端、不用改頁面，純靜態打包進前端。
//
// ── 怎麼新增一筆 ─────────────────────────────────────────────────────────
// 筆試缺考統計（沒有題目，重點是 absent 人數）：
//   {
//     id: 'mega-written-20260510',
//     bank: '兆豐銀行',
//     stage: 'written',
//     date: '2026-05-10',
//     sessions: [
//       { room: '第 1 試場', absent: 3 },
//       { room: '第 2 試場', absent: 0 },   // 0 = 無缺考，也會顯示
//     ],
//   }
//
// 面試情報（每個試場一串題目；缺考可填可不填）：
//   {
//     id: 'land-interview-20260720',
//     bank: '土地銀行',
//     stage: 'interview',
//     date: '2026-07-20',
//     sessions: [
//       { room: '第 1 試場', grade: '土八', questions: ['題目一', '題目二'] },
//     ],
//     extras: ['未標試場的零散題'],
//   }
//
// 欄位沒有就不用寫（grade / absent / questions / extras / note 都是選填）。

/** 招考階段：筆試 / 面試。 */
export type VenueStage = 'written' | 'interview';

export interface VenueSession {
  /** 試場名稱，例如「第 1 試場」。 */
  room: string;
  /** 報考職等，例如「兆八」「兆六」；面試常用，筆試可省略。 */
  grade?: string;
  /** 缺考人數（0 代表無缺考、也會顯示；不知道就省略）。 */
  absent?: number;
  /** 面試題目 / 考驗重點；筆試通常沒有，可省略。 */
  questions?: string[];
  /** 該試場的其他備註，可省略。 */
  note?: string;
}

export interface VenueReport {
  /** 穩定的唯一 id（也用於網址 hash 定位）。 */
  id: string;
  /** 銀行名稱，例如「兆豐銀行」。 */
  bank: string;
  /** 階段：'written' 筆試 / 'interview' 面試。 */
  stage: VenueStage;
  /** 招考 / 面試日期（YYYY-MM-DD）。 */
  date: string;
  /** 各試場資訊。 */
  sessions: VenueSession[];
  /** 沒有明確標示試場、但值得參考的零散考題（多用於面試）。 */
  extras?: string[];
  /** 資料來源 / 整理備註，可省略。 */
  note?: string;
}

